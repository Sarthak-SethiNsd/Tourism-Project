import type { TourismCategoryId, TourismPlace, TourismPriceLevel } from "@/types/tourism";

const imagePlaceholder = "/images/india-discovery-hero.png";
const coordinatePlaceholder = { latitude: null, longitude: null };

type ArunachalPlaceInput = {
  districtName: string;
  destinationSlug: string;
  name: string;
  categoryIds: TourismCategoryId[];
  summary: string;
  bestTimeToVisit: string;
  priceLevel: TourismPriceLevel;
  idealDuration: string;
  tags: string[];
};

function districtIdFromName(districtName: string) {
  return `arunachal-pradesh-${districtName
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")}`;
}

function createArunachalPlace({
  districtName,
  destinationSlug,
  name,
  categoryIds,
  summary,
  bestTimeToVisit,
  priceLevel,
  idealDuration,
  tags,
}: ArunachalPlaceInput): TourismPlace {
  return {
    id: `arunachal-pradesh-${districtIdFromName(districtName).replace("arunachal-pradesh-", "")}-${destinationSlug}`,
    name,
    stateId: "arunachal-pradesh",
    districtId: districtIdFromName(districtName),
    districtName,
    categoryIds,
    summary,
    description: summary,
    highlights: ["Scenic local experience", "Culture and nature stop", "Offline dataset entry"],
    bestTimeToVisit,
    idealDuration,
    priceLevel,
    approximateBudget: priceLevel,
    rating: 4.3,
    tags: ["arunachal-pradesh", districtName.toLowerCase(), ...tags],
    imageUrl: imagePlaceholder,
    coordinates: coordinatePlaceholder,
  };
}

